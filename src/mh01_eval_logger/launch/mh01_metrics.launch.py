from launch import LaunchDescription
from launch.actions import DeclareLaunchArgument
from launch.substitutions import LaunchConfiguration
from launch_ros.actions import Node
from launch_ros.parameter_descriptions import ParameterValue


def generate_launch_description():
    return LaunchDescription([
        DeclareLaunchArgument(
            'ground_truth_csv',
            default_value='/home/hashizume/experiment/MH_01_easy_leica_position.csv',
            description='Path to the exported /leica/position ground truth CSV.',
        ),
        DeclareLaunchArgument(
            'eval_log_csv',
            default_value=(
                '/home/hashizume/experiment/results/'
                'mh01_eval_log_20260528_150755.csv'
            ),
            description='Path to a CSV generated by mh01_eval_logger.',
        ),
        DeclareLaunchArgument(
            'output_dir',
            default_value='/home/hashizume/experiment/results',
            description='Directory for metrics summary and plot outputs.',
        ),
        DeclareLaunchArgument(
            'relative_window_sec',
            default_value='1.0',
            description='Window length used for avgRTE and avgRE relative errors.',
        ),
        DeclareLaunchArgument(
            'max_match_dt_sec',
            default_value='0.5',
            description='Skip samples whose ground truth match is farther than this value.',
        ),
        DeclareLaunchArgument(
            'plot',
            default_value='true',
            description='Whether to save a trajectory/error PNG plot.',
        ),
        Node(
            package='mh01_eval_logger',
            executable='mh01_metrics',
            name='mh01_metrics_node',
            output='screen',
            parameters=[{
                'ground_truth_csv': LaunchConfiguration('ground_truth_csv'),
                'eval_log_csv': LaunchConfiguration('eval_log_csv'),
                'output_dir': LaunchConfiguration('output_dir'),
                'relative_window_sec': ParameterValue(
                    LaunchConfiguration('relative_window_sec'),
                    value_type=float,
                ),
                'max_match_dt_sec': ParameterValue(
                    LaunchConfiguration('max_match_dt_sec'),
                    value_type=float,
                ),
                'plot': ParameterValue(
                    LaunchConfiguration('plot'),
                    value_type=bool,
                ),
            }],
        ),
    ])
