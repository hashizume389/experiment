from launch import LaunchDescription
from launch.actions import DeclareLaunchArgument
from launch.substitutions import LaunchConfiguration
from launch_ros.actions import Node
from launch_ros.parameter_descriptions import ParameterValue


def generate_launch_description():
    return LaunchDescription([
        DeclareLaunchArgument(
            'ground_truth_csv',
            default_value=(
                '/home/hashizume/experiment/MH_01_easy/'
                'mav0/state_groundtruth_estimate0/data.csv'
            ),
            description=(
                'Path to EuRoC state_groundtruth_estimate0/data.csv with position '
                'and quaternion ground truth.'
            ),
        ),
        DeclareLaunchArgument(
            'eval_log_csv',
            default_value=(
                '/home/hashizume/experiment/results/'
                'mh01_eval_log_20260528_150755.csv'
            ),
            description='Path to a CSV generated by mh01_eval_logger.',
        ),
        DeclareLaunchArgument(
            'output_dir',
            default_value='/home/hashizume/experiment/results',
            description='Directory for metrics summary and plot outputs.',
        ),
        DeclareLaunchArgument(
            'relative_delta',
            default_value='1.0',
            description='Relative error delta. Interpreted by relative_delta_unit.',
        ),
        DeclareLaunchArgument(
            'relative_delta_unit',
            default_value='m',
            description='Relative error delta unit: m, s, or frames.',
        ),
        DeclareLaunchArgument(
            'relative_window_sec',
            default_value='1.0',
            description='Fallback window length when relative_delta_unit is s.',
        ),
        DeclareLaunchArgument(
            'alignment',
            default_value='sim3',
            description='Trajectory alignment for APE/RPE positions: sim3 or se3.',
        ),
        DeclareLaunchArgument(
            'max_match_dt_sec',
            default_value='0.05',
            description='Skip samples whose ground truth match is farther than this value.',
        ),
        DeclareLaunchArgument(
            'min_relative_distance_m',
            default_value='0.1',
            description='Skip RPE pairs with shorter ground truth distance.',
        ),
        DeclareLaunchArgument(
            'segment_end_sec',
            default_value='50.0',
            description='Also evaluate the initial segment ending at this elapsed time.',
        ),
        DeclareLaunchArgument(
            'jump_threshold_m',
            default_value='0.5',
            description='Report odometry steps larger than this value as likely jumps.',
        ),
        DeclareLaunchArgument(
            'plot',
            default_value='true',
            description='Whether to save a trajectory/error PNG plot.',
        ),
        Node(
            package='mh01_eval_logger',
            executable='mh01_metrics',
            name='mh01_metrics_node',
            output='screen',
            parameters=[{
                'ground_truth_csv': LaunchConfiguration('ground_truth_csv'),
                'eval_log_csv': LaunchConfiguration('eval_log_csv'),
                'output_dir': LaunchConfiguration('output_dir'),
                'relative_delta': ParameterValue(
                    LaunchConfiguration('relative_delta'),
                    value_type=float,
                ),
                'relative_delta_unit': LaunchConfiguration('relative_delta_unit'),
                'relative_window_sec': ParameterValue(
                    LaunchConfiguration('relative_window_sec'),
                    value_type=float,
                ),
                'alignment': LaunchConfiguration('alignment'),
                'min_relative_distance_m': ParameterValue(
                    LaunchConfiguration('min_relative_distance_m'),
                    value_type=float,
                ),
                'max_match_dt_sec': ParameterValue(
                    LaunchConfiguration('max_match_dt_sec'),
                    value_type=float,
                ),
                'segment_end_sec': ParameterValue(
                    LaunchConfiguration('segment_end_sec'),
                    value_type=float,
                ),
                'jump_threshold_m': ParameterValue(
                    LaunchConfiguration('jump_threshold_m'),
                    value_type=float,
                ),
                'plot': ParameterValue(
                    LaunchConfiguration('plot'),
                    value_type=bool,
                ),
            }],
        ),
    ])
