"""MH_01 EuRoC LDFE preprocessing launch file.

This launch starts one LDFE node for each rectified EuRoC stereo image stream.
Use it with the rectified/restamped MH_01 bag generated by:

  scripts/rectify_euroc_stereo_bag.py
  scripts/restamp_ros2_bag_by_header.py

The CameraInfo topics are not modified by this pipeline. For Isaac ROS Visual
SLAM, remap images to the LDFE output topics and keep CameraInfo topics from the
rectified bag.
"""

from launch import LaunchDescription
from launch.actions import DeclareLaunchArgument
from launch.substitutions import LaunchConfiguration
from launch_ros.actions import Node
from launch_ros.parameter_descriptions import ParameterValue


def ldfe_node(name, image_in, image_out):
    return Node(
        package='infra_enhancer',
        executable='ldfe_node',
        name=name,
        remappings=[
            ('image_in', image_in),
            ('image_out', image_out),
        ],
        parameters=[{
            'brightness_dark': ParameterValue(
                LaunchConfiguration('brightness_dark'),
                value_type=float,
            ),
            'brightness_bright': ParameterValue(
                LaunchConfiguration('brightness_bright'),
                value_type=float,
            ),
            'clahe_clip_min': ParameterValue(
                LaunchConfiguration('clahe_clip_min'),
                value_type=float,
            ),
            'clahe_clip_max': ParameterValue(
                LaunchConfiguration('clahe_clip_max'),
                value_type=float,
            ),
            'gamma_min': ParameterValue(
                LaunchConfiguration('gamma_min'),
                value_type=float,
            ),
            'gamma_max': ParameterValue(
                LaunchConfiguration('gamma_max'),
                value_type=float,
            ),
            'clahe_tile_grid_size': ParameterValue(
                LaunchConfiguration('clahe_tile_grid_size'),
                value_type=int,
            ),
        }],
    )


def generate_launch_description():
    return LaunchDescription([
        DeclareLaunchArgument(
            'left_image_in',
            default_value='/cam0/image_rect',
            description='Rectified MH_01 left image topic.',
        ),
        DeclareLaunchArgument(
            'right_image_in',
            default_value='/cam1/image_rect',
            description='Rectified MH_01 right image topic.',
        ),
        DeclareLaunchArgument(
            'left_image_out',
            default_value='/mh01/cam0/image_ldfe',
            description='LDFE-processed left image topic.',
        ),
        DeclareLaunchArgument(
            'right_image_out',
            default_value='/mh01/cam1/image_ldfe',
            description='LDFE-processed right image topic.',
        ),
        DeclareLaunchArgument(
            'brightness_dark',
            default_value='50.0',
            description='Mean brightness treated as low light.',
        ),
        DeclareLaunchArgument(
            'brightness_bright',
            default_value='180.0',
            description='Mean brightness treated as bright enough.',
        ),
        DeclareLaunchArgument(
            'clahe_clip_min',
            default_value='1.0',
            description='Minimum CLAHE clip limit.',
        ),
        DeclareLaunchArgument(
            'clahe_clip_max',
            default_value='5.0',
            description='Maximum CLAHE clip limit.',
        ),
        DeclareLaunchArgument(
            'gamma_min',
            default_value='0.5',
            description='Gamma value used in low light.',
        ),
        DeclareLaunchArgument(
            'gamma_max',
            default_value='1.2',
            description='Gamma value used in bright images.',
        ),
        DeclareLaunchArgument(
            'clahe_tile_grid_size',
            default_value='8',
            description='CLAHE tile grid size.',
        ),
        ldfe_node(
            'mh01_ldfe_cam0',
            LaunchConfiguration('left_image_in'),
            LaunchConfiguration('left_image_out'),
        ),
        ldfe_node(
            'mh01_ldfe_cam1',
            LaunchConfiguration('right_image_in'),
            LaunchConfiguration('right_image_out'),
        ),
    ])
